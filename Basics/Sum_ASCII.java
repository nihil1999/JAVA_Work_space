//ASCII value char to int
class Sum_ASCII
{
    public static void main(String arg[])
        {
                int a='a';
                int b='b';
                int c=a+b;
                
                char a1=93;//'1';
                char b1=73;//'2';
               // int d=a1;
               // int e=b1;
                int sum=a1+b1;
                
                System.out.println("a= " +a);
                System.out.println("b= " +b);    
                System.out.println("c= " +c);

                System.out.println("a1= " +a1);
                System.out.println("b1= " +b1);    
                System.out.println("sum= " +sum);
        }
}
